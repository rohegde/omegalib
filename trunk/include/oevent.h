#ifndef __OEVENT_H__
#define __OEVENT_H__

#include "osystem.h"
#include "ocfg.h"

/**********************************************************************************************************************
*/
enum OMEventType
{
	OM_EVENT_MOVE,
	OM_EVENT_DOWN,
	OM_EVENT_UP,
	OM_EVENT_TRACE,
	OM_EVENT_UNTRACE,
};

/**********************************************************************************************************************
*/
typedef struct _OMEvent
{
    unsigned int id;
	unsigned int flags;
    enum OMEventType type;
    enum OMDeviceClass source;
    float x;
	float y;
	float z;
    float rx;
	float ry;
	float rz;
} OMEvent;

/**********************************************************************************************************************
*/
void oeventInit(const OMConfig* cfg);

/**********************************************************************************************************************
*/
int oeventGetAvailableEvents();

/**********************************************************************************************************************
*/
int oeventGetDroppedEvents();

/**********************************************************************************************************************
*/
void oeventResetDroppedEvents();

/**********************************************************************************************************************
*/
void oeventLock();

/**********************************************************************************************************************
*/
void oeventUnlock();

/**********************************************************************************************************************
*/
OMEvent* oeventReadTail();

/**********************************************************************************************************************
*/
OMEvent* oeventReadHead();

/**********************************************************************************************************************
*/
OMEvent* oeventWriteHead();

#endif